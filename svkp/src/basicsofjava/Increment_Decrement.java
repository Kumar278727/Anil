package basicsofjava;

public class Increment_Decrement {

	public static void main(String[] args) {
		int a=10;
		
		System.out.println("a++  " + a++);
		System.out.println("after post-increment a   "+ a); 
		
		
		System.out.println("++a  " + ++a);
		
		
		System.out.println("a--  " + a--);
		System.out.println("after post decrement a   " + a); 
		
		
		System.out.println("--a "+ --a);
	}


	}


